//Write a program in C TO PRINT YOUR NAMES 10 TIMES using for loop.

#include <stdio.h>
int main() {
    int i = 0; // Initialize the variable i with 0

    // Use a while loop to repeat the printing statement 100 times
    while (i < 10) {
        // Print the string "Bidusha"
        printf("Bidusha\n");

        i = i + 1; // Increment the value of i by 1
    }

    return 0; 
}

