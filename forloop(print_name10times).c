//Write a program in C TO PRINT YOUR NAMES 10 TIMES using for loop.

#include <stdio.h>

int main() {
    // Declare variable i for loop control
    int i;

    // Use a for loop to repeat the printing statement 100 times
    for (i = 0; i < 10; i++) {
        // Print the string "Bidusha" followed by a newline
        printf("Bidusha\n");
    }

    // Return 0 to indicate successful completion
    return 0;
}
